package com.march13;

public class Friends {
	private String age;
	private String name;
	@Override
	public String toString() {
		return "Friends [age=" + age + ", name=" + name + "]";
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
		

	

}
